package com.msu.DTO;

public class Schedule {
	
	
	private Occupency Mon;
	private Occupency Tues;
	private Occupency Wed;
	private Occupency Thurs;
	private Occupency Fri;
	
	public Occupency getMon() {
		return Mon;
	}
	public void setMon(Occupency mon) {
		Mon = mon;
	}
	public Occupency getTues() {
		return Tues;
	}
	public void setTues(Occupency tues) {
		Tues = tues;
	}
	public Occupency getWed() {
		return Wed;
	}
	public void setWed(Occupency wed) {
		Wed = wed;
	}
	public Occupency getThurs() {
		return Thurs;
	}
	public void setThurs(Occupency thurs) {
		Thurs = thurs;
	}
	public Occupency getFri() {
		return Fri;
	}
	public void setFri(Occupency fri) {
		Fri = fri;
	}
	
	

}
